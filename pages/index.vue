<script>
import axios from 'axios'

export default {
  data() {
    return {
      email: '',
      errorMsg: '',
    }
  },
  methods: {
    closePopup () {
      const el = document.querySelector('.success-popup')
      el.className = 'success-popup'
    },
    emailChange () {
      this.errorMsg = !this.validEmail(this.email) ? 'Valid email required.' : ''
    },
    validEmail:function(email) {
      const re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
      return re.test(email)
    },
    addEmailToSubscriptionList () {
      const el = document.querySelector('.success-popup')
      axios({
        url: "https://api.node.nyc/api/email-subscrption/playa",
        method: 'post',
        data: {
          "email": this.email
        }
      })
      .then(function(response) {
        if (response) {
          console.log(response)
          el.className += ' active'
        }
      })
      .then(function(error) {
        if (error) console.log(error)
      })
    },
    submit () {
      if (this.validEmail(this.email)) {
        this.addEmailToSubscriptionList()
      } else {
        console.log('Valid email required.')
      }
    }
  }
}
</script>

